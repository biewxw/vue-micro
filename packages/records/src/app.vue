<template>
  <router-view />
</template>

<script lang="ts">
import { defineComponent } from 'vue';
import { useRouter } from 'vue-router';

export default defineComponent({
  setup() {
    const router = useRouter();

    if (!window.localStorage.getItem('AUTH:LOGGED')) {
      router.replace('/auth');
    }
  },
});
</script>

